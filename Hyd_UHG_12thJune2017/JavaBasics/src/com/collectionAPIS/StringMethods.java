package com.collectionAPIS;

public class StringMethods {

	public static void main(String[] args) {
			String str="Hyd is too hot now days";
			System.out.println(str.length());
			System.out.println(str.equals("hyd is too hot now days"));
			System.out.println(str.equalsIgnoreCase("hyd is too hot now days"));
			System.out.println(str.contains("2"));
			System.out.println(str.isEmpty());
			
		
	}

}
